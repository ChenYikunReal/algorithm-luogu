bool prime(int a) {
    if (a == 1) {
        return false;
    }
    for (int i = 2; i*i <= a; i++) {
        if (!(a % i)) {
            return false;
        }
    }
    return true;
}

bool check(int a) {
    while (a) {
        if (!prime(a)) {
            return false;
        }
        a /= 10;
    }
    return true;
}

